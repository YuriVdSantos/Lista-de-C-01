#include <unistd.h>

void ft_putnbr(int nb)
{
	char i;
	
	if (nb == -2147483647)
	{
		write(1, "-2147483647", 11);
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	i = nb % 10 + '0';
	write(1, &i, 1);
}

int main(){
	ft_putnbr(-2);
}